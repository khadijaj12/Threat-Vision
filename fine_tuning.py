# ── Dataset class ─────────────────────────────────────────────────────────
class PersonSegDataset(Dataset):
    def __init__(self, img_dir, mask_dir, transform=None):
        self.images    = sorted(glob.glob(f"{img_dir}/*.jpg"))
        self.masks     = sorted(glob.glob(f"{mask_dir}/*.png"))
        self.transform = transform
        assert len(self.images) == len(self.masks), \
            f"Image/mask count mismatch: {len(self.images)} vs {len(self.masks)}"

    def __len__(self):
        return len(self.images)

    def __getitem__(self, idx):
        img  = cv2.cvtColor(cv2.imread(self.images[idx]), cv2.COLOR_BGR2RGB)
        mask = cv2.imread(self.masks[idx], cv2.IMREAD_GRAYSCALE)
        mask = (mask > 127).astype(np.float32)  # binary 0/1

        if self.transform:
            aug  = self.transform(image=img, mask=mask)
            img  = aug["image"]
            mask = aug["mask"]

        return img, mask.unsqueeze(0) if isinstance(mask, torch.Tensor) else torch.tensor(mask).unsqueeze(0)

