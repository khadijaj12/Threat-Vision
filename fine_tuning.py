def polygon_to_mask(polygons, height, width):
    """Convert COCO polygon segmentation annotations to binary mask."""
    mask = np.zeros((height, width), dtype=np.uint8)
    for poly in polygons:
        pts = np.array(poly, dtype=np.float32).reshape(-1, 1, 2).astype(np.int32)
        cv2.fillPoly(mask, [pts], 1)
    return mask

def build_unet_split(fo_dataset, split_name, target_size=(384, 384)):
    """Extract image + segmentation mask pairs from a FiftyOne dataset."""
    saved = 0
    skipped = 0

    for i, sample in enumerate(tqdm(fo_dataset, desc=f"Building {split_name} U-Net data")):
        img_path = sample.filepath
        if not os.path.exists(img_path):
            skipped += 1
            continue

        img = cv2.imread(img_path)
        if img is None:
            skipped += 1
            continue

        h, w = img.shape[:2]
        combined_mask = np.zeros((h, w), dtype=np.uint8)

        # Get all person segmentation polygons
        if sample.segmentations is None:
            skipped += 1
            continue

        has_person = False
        for det in sample.segmentations.detections:
            if det.label != "person":
                continue
            if det.mask is None:
                continue

            # FiftyOne stores masks as relative bounding box crops
            # Convert back to full image coordinates
            x1 = int(det.bounding_box[0] * w)
            y1 = int(det.bounding_box[1] * h)
            bw = int(det.bounding_box[2] * w)
            bh = int(det.bounding_box[3] * h)
            x2 = min(w, x1 + bw)
            y2 = min(h, y1 + bh)

            if bw <= 0 or bh <= 0:
                continue

            mask_crop = det.mask.astype(np.uint8) * 255
            mask_resized = cv2.resize(mask_crop, (x2-x1, y2-y1), interpolation=cv2.INTER_NEAREST)
            combined_mask[y1:y2, x1:x2] = np.maximum(
                combined_mask[y1:y2, x1:x2],
                mask_resized
            )
            has_person = True

        if not has_person:
            skipped += 1
            continue

        # Resize both image and mask to U-Net input size
        img_resized  = cv2.resize(img, target_size)
        mask_resized = cv2.resize(combined_mask, target_size, interpolation=cv2.INTER_NEAREST)

        # Save
        fname = f"{i:06d}.jpg"
        cv2.imwrite(f"{UNET_DIR}/{split_name}/images/{fname}", img_resized)
        cv2.imwrite(f"{UNET_DIR}/{split_name}/masks/{fname.replace('.jpg','.png')}", mask_resized)
        saved += 1

    print(f"  Saved: {saved} | Skipped: {skipped}")
    return saved

