infer_transform = A.Compose([
    A.Resize(384, 384),
    A.Normalize(mean=[0.485,0.456,0.406], std=[0.229,0.224,0.225]),
    ToTensorV2(),
])

for row, idx in enumerate(indices):
    img_raw  = cv2.cvtColor(cv2.imread(val_images[idx]), cv2.COLOR_BGR2RGB)
    mask_raw = cv2.imread(val_masks[idx], cv2.IMREAD_GRAYSCALE)

    aug = infer_transform(image=img_raw, mask=(mask_raw > 127).astype(np.float32))
    inp = aug["image"].unsqueeze(0).to(DEVICE)

    with torch.no_grad():
        pred = torch.sigmoid(unet(inp)).squeeze().cpu().numpy()

    pred_binary = (pred > 0.5).astype(np.uint8) * 255

    # Overlay on image
    # Convert img_raw to float for blending calculations and normalize to 0-1 range
    img_raw_float = img_raw.astype(np.float32) / 255.0

    # Expand pred_binary to have a channel dimension (H, W, 1) and normalize to 0 or 1
    # This acts as an alpha mask for blending
    pred_mask_3ch = np.expand_dims(pred_binary, axis=-1).astype(np.float32) / 255.0

    # Define the red color, normalized to 0-1
    red_color = np.array([1.0, 0.0, 0.0]) # Red

    # Perform alpha blending using np.where
    # If pred_mask_3ch is > 0 (i.e., mask pixel is white), blend red with img_raw_float
    # Otherwise, keep img_raw_float
    # The original blend was `img * 0.5 + red * 0.5` where mask is true
    overlay_float = np.where(
        pred_mask_3ch > 0, # Condition: where the mask is active
        img_raw_float * 0.5 + red_color * 0.5, # Blended color (50% original, 50% red)
        img_raw_float # Original image where mask is inactive
    )

    # Convert back to uint8 and scale to 0-255
    overlay = (overlay_float * 255).astype(np.uint8)

    axes[row][0].imshow(img_raw)
    axes[row][0].set_title("Input")
    axes[row][0].axis("off")

    axes[row][1].imshow(mask_raw, cmap="gray")
    axes[row][1].set_title("Ground Truth")
    axes[row][1].axis("off")

    axes[row][2].imshow(overlay) # Display the blended overlay
    axes[row][2].set_title(f"Prediction (Overlay)")
    axes[row][2].axis("off")

